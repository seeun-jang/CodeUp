a, b = map(float, input().split())
print(f"{a*b:.2f}")

# 입력받은 값을 공백을 기준으로 나눈다.
# 예를 들어 "3.2 4.5"를 입력하면 ['3.2', '4.5']가 된다.
# map(float, ...)는 각각의 문자열을 실수(float)로 변환한다.
# 변환된 값을 a와 b에 저장한다.
# a, b = map(float, input().split())

