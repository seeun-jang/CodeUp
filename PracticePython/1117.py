# f""는 문자열 안에 변수나 계산 결과를 넣을 수 있는 f-string이다.
# {a*b:.2f}에서
#   a*b   : 두 수를 곱한다.
#   :     : 출력 형식을 지정하겠다는 의미이다.
#   .2f   : 소수점 둘째 자리까지 출력한다.
